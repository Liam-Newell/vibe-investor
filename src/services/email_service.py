    async def send_morning_report(self, 
                                opportunities: List[Dict],
                                portfolio: PortfolioSummary,
                                market_data: Dict[str, Any],
                                claude_analysis: str) -> bool:
        """Send morning strategy report"""
        
        if not self.smtp_config["enabled"]:
            logger.info("üìß Email disabled - Morning report not sent")
            return False
            
        try:
            # Generate beautiful report with Claude
            report_html = await self._generate_morning_report_html(
                opportunities, portfolio, market_data, claude_analysis
            )
            
            subject = f"üåÖ Vibe Investor Morning Report - {datetime.now().strftime('%Y-%m-%d')}"
            
            success = await self._send_email(
                to_email="liam-newell@hotmail.com",
                subject=subject,
                html_content=report_html
            )
            
            if success:
                logger.info("üìß ‚úÖ Morning report sent successfully")
            else:
                logger.error("üìß ‚ùå Failed to send morning report")
                
            return success
            
        except Exception as e:
            logger.error(f"üìß ‚ùå Morning report error: {e}")
            return False
    
